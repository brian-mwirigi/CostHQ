/* ── Component ──────────────────────────────────────── */

export default function ShareCard() {
  const [stats, setStats] = useState<Stats | null>(null);
  const [models, setModels] = useState<ModelBreakdown[]>([]);
  const [copied, setCopied] = useState(false);
  const canvasRef = useRef<HTMLCanvasElement | null>(null);

  useEffect(() => {
    fetchApi<Stats>('/api/stats').then(setStats).catch(() => {});
    fetchApi<ModelBreakdown[]>('/api/model-breakdown').then(setModels).catch(() => {});
  }, []);

  const topModel = models.length > 0
    ? models.reduce((top, m) => (m.totalCost > top.totalCost ? m : top), models[0])
    : null;

  const handleDownload = useCallback(() => {
    if (!stats) return;
    const canvas = drawShareImage(stats, topModel);
    canvasRef.current = canvas;

    const link = document.createElement('a');
    link.download = 'codesession-stats.png';
    link.href = canvas.toDataURL('image/png');
    link.click();
  }, [stats, topModel]);

  const handleCopy = useCallback(async () => {
    if (!stats) return;
    const canvas = drawShareImage(stats, topModel);
    canvasRef.current = canvas;

    try {
      const blob = await new Promise<Blob | null>((resolve) =>
        canvas.toBlob(resolve, 'image/png'),
      );
      if (blob) {
        await navigator.clipboard.write([
          new ClipboardItem({ 'image/png': blob }),
        ]);
        setCopied(true);
        setTimeout(() => setCopied(false), 2000);
      }
    } catch {
      // Fallback: just download instead
      handleDownload();
    }
  }, [stats, topModel, handleDownload]);

  if (!stats) {
    return <div className="loading">Loading…</div>;
  }

  const statItems: [string, string][] = [
    ['Sessions', String(stats.totalSessions)],
    ['Total Cost', formatCost(stats.totalAICost)],
    ['Total Time', formatDuration(stats.totalTime)],
    ['Files Changed', String(stats.totalFiles)],
    ['Commits', String(stats.totalCommits)],
    ['Avg Session', formatDuration(Math.round(stats.avgSessionTime))],
  ];

  return (
    <div className="page">
      <div className="page-header">
        <h1 className="page-title">Share Your Stats</h1>
        <div className="page-subtitle">
          Generate a shareable image of your AI coding journey
        </div>
      </div>

      {/* Live preview card */}
      <div style={styles.preview}>
        <div style={styles.previewGrid} />
        <div style={styles.previewGlow} />

        <div style={styles.inner}>
          <div style={styles.brand}>CODESESSION</div>
          <h2 style={styles.title}>My AI Coding Stats</h2>
          <div style={styles.divider} />

          <div style={styles.grid}>
            {statItems.map(([label, value]) => (
              <div key={label} style={styles.statBox}>
                <div style={styles.statLabel}>{label}</div>
                <div style={styles.statVal}>{value}</div>
              </div>
            ))}
          </div>

          {topModel && (
            <div style={styles.modelBadge}>
              <div>
                <div style={styles.modelLabel}>⚡ TOP MODEL</div>
                <div style={styles.modelName}>
                  {topModel.provider} / {topModel.model}
                </div>
              </div>
              <div style={styles.modelCost}>{formatCost(topModel.totalCost)}</div>
            </div>
          )}

          <div style={styles.footer}>Generated by CodeSession</div>
        </div>
      </div>

      {/* Action buttons */}
      <div style={styles.actions}>
        <button
          style={{ ...styles.btn, ...styles.btnPrimary }}
          onClick={handleDownload}
        >
          <DownloadIcon />
          Download PNG
        </button>
        <button style={styles.btn} onClick={handleCopy}>
          {copied ? <CheckIcon /> : <CopyIcon />}
          {copied ? 'Copied!' : 'Copy as Image'}
        </button>
      </div>
    </div>
  );
}

