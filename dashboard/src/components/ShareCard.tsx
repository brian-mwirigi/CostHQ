/* ── Types ──────────────────────────────────────────── */

interface Stats {
  totalSessions: number;
  totalTime: number;
  totalFiles: number;
  totalCommits: number;
  totalAICost: number;
  avgSessionTime: number;
  activeSessions: number;
}

interface ModelBreakdown {
  provider: string;
  model: string;
  totalCost: number;
  totalTokens: number;
  usageCount: number;
}

/* ── Canvas drawing helpers ─────────────────────────── */

const CARD_W = 720;
const CARD_H = 520;

function drawShareImage(stats: Stats, topModel: ModelBreakdown | null): HTMLCanvasElement {
  const canvas = document.createElement('canvas');
  const dpr = window.devicePixelRatio || 2;
  canvas.width = CARD_W * dpr;
  canvas.height = CARD_H * dpr;
  canvas.style.width = `${CARD_W}px`;
  canvas.style.height = `${CARD_H}px`;

  const ctx = canvas.getContext('2d')!;
  ctx.scale(dpr, dpr);

  // ── Background (Vercel/Linear style dark root)
  ctx.fillStyle = '#09090b'; // var(--bg-root)
  roundRect(ctx, 0, 0, CARD_W, CARD_H, 12);
  ctx.fill();

  // ── Branding (Subtle, neutral)
  ctx.fillStyle = '#a1a1aa'; // var(--text-secondary)
  ctx.font = '600 12px Inter, system-ui, sans-serif';
  ctx.letterSpacing = '1px';
  ctx.fillText('CODESESSION', 40, 50);
  ctx.letterSpacing = '0px';

  // ── Title
  ctx.fillStyle = '#fafafa'; // var(--text-primary)
  ctx.font = '600 28px Inter, system-ui, sans-serif';
  ctx.fillText('My AI Coding Stats', 40, 95);

  // ── Divider
  ctx.fillStyle = '#27272a'; // var(--border)
  ctx.fillRect(40, 120, CARD_W - 80, 1);

  // ── Stat grid (2 columns × 3 rows)
  const items: [string, string][] = [
    ['Sessions', String(stats.totalSessions)],
    ['Total Cost', formatCost(stats.totalAICost)],
    ['Total Time', formatDuration(stats.totalTime)],
    ['Files Changed', String(stats.totalFiles)],
    ['Commits', String(stats.totalCommits)],
    ['Avg Session', formatDuration(Math.round(stats.avgSessionTime))],
  ];

  const colW = 310;
  const startX = 40;
  const startY = 150;
  const rowH = 80;

  items.forEach(([label, value], i) => {
    const col = i % 2;
    const row = Math.floor(i / 2);
    const x = startX + col * colW;
    const y = startY + row * rowH;

    ctx.fillStyle = '#111113'; // var(--bg-surface)
    roundRect(ctx, x, y, 280, 64, 8);
    ctx.fill();
    
    ctx.strokeStyle = '#27272a'; // var(--border)
    ctx.lineWidth = 1;
    ctx.stroke();

    ctx.fillStyle = '#71717a'; // var(--text-tertiary)
    ctx.font = '500 11px Inter, system-ui, sans-serif';
    ctx.fillText(label.toUpperCase(), x + 16, y + 24);

    ctx.fillStyle = '#fafafa'; // var(--text-primary)
    ctx.font = '600 20px Inter, system-ui, sans-serif';
    ctx.fillText(value, x + 16, y + 50);
  });

  // ── Top model badge
  if (topModel) {
    const badgeY = startY + 3 * rowH + 10;
    
    ctx.fillStyle = '#111113'; // var(--bg-surface)
    roundRect(ctx, 40, badgeY, CARD_W - 80, 60, 8);
    ctx.fill();

    ctx.strokeStyle = '#27272a'; // var(--border)
    ctx.lineWidth = 1;
    ctx.stroke();

    ctx.fillStyle = '#a1a1aa'; // var(--text-secondary)
    ctx.font = '500 11px Inter, system-ui, sans-serif';
    ctx.fillText('⚡ TOP MODEL', 56, badgeY + 24);

    ctx.fillStyle = '#fafafa'; // var(--text-primary)
    ctx.font = '600 16px Inter, system-ui, sans-serif';
    const modelLabel = `${topModel.provider} / ${topModel.model}`;
    ctx.fillText(modelLabel, 56, badgeY + 45);

    ctx.fillStyle = '#a1a1aa'; // var(--text-secondary)
    ctx.font = '14px Inter, system-ui, sans-serif';
    const costLabel = formatCost(topModel.totalCost);
    ctx.textAlign = 'right';
    ctx.fillText(costLabel, CARD_W - 56, badgeY + 36);
    ctx.textAlign = 'left';
  }

  // ── Footer watermark
  ctx.fillStyle = '#52525b'; // var(--gray-600)
  ctx.font = '11px Inter, system-ui, sans-serif';
  ctx.textAlign = 'right';
  ctx.fillText('Generated by CodeSession', CARD_W - 40, CARD_H - 24);
  ctx.textAlign = 'left';

  return canvas;
}

function roundRect(
  ctx: CanvasRenderingContext2D,
  x: number, y: number, w: number, h: number, r: number,
) {
  ctx.beginPath();
  ctx.moveTo(x + r, y);
  ctx.lineTo(x + w - r, y);
  ctx.quadraticCurveTo(x + w, y, x + w, y + r);
  ctx.lineTo(x + w, y + h - r);
  ctx.quadraticCurveTo(x + w, y + h, x + w - r, y + h);
  ctx.lineTo(x + r, y + h);
  ctx.quadraticCurveTo(x, y + h, x, y + h - r);
  ctx.lineTo(x, y + r);
  ctx.quadraticCurveTo(x, y, x + r, y);
  ctx.closePath();
}

